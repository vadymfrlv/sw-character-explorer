export interface FetchCharacterResponse {
  count: number;
  next: string;
  previous: string;
  results: Character[];
}

export interface Character {
  name: string;
  height: string;
  mass: string;
  hair_color: string;
  skin_color: string;
  eye_color: string;
  birth_year: string;
  gender: string;
  homeworld: string;
  films: string[];
  species: string[];
  vehicles: string[];
  starships: string[];
  created: string;
  edited: string;
  url: string;
}

export type Classification =
  | 'mammal'
  | 'artificial'
  | 'sentient'
  | 'gastropod'
  | 'reptile'
  | 'amphibian'
  | 'unknown';

export interface Species {
  name: string;
  classification: Classification;
  designation: string;
  average_height: string;
  skin_colors: string;
  hair_colors: string;
  eye_colors: string;
  average_lifespan: string;
  homeworld: string | null;
  language: string;
  people: string[];
  films: string[];
  created: string;
  edited: string;
  url: string;
}

